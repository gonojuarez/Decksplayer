    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      
        if(cancion.getActions().isStopped()||cancion.getActions().isUnknow()){
         cancion.getActions().play(cancion.getDireccion()); //play the file with the path direction
         loadIcon();
        }
        else if(cancion.getActions().isPlaying()){
         cancion.getActions().pause(); //pause the file+
          loadIcon();
        }
        else if(cancion.getActions().isPause()){
         cancion.getActions().resume(); //resume the file
         loadIcon();
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

