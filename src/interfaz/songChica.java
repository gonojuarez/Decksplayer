    public void loadIcon(){
         if(cancion.getActions().isStopped()){
            ImageIcon icon=new ImageIcon(getClass().getResource("/dj/Imagenes/play.png"));
         jButton3.setIcon(icon);
        }else if(cancion.getActions().isUnknow()){
             ImageIcon icon=new ImageIcon(getClass().getResource("/dj/Imagenes/play.png"));
         jButton3.setIcon(icon);
        }
        else if(cancion.getActions().isPlaying()){
            ImageIcon icon=new ImageIcon(getClass().getResource("/dj/Imagenes/pausa.png"));
         jButton3.setIcon(icon);
        }
        else if(cancion.getActions().isPause()){
          ImageIcon icon=new ImageIcon(getClass().getResource("/dj/Imagenes/play.png"));
         jButton3.setIcon(icon);
        } 
    }

